    private boolean dataCheck() {

        if (jTextField1.getText().equals("Usuario") && jPasswordField1.getText().equals("Contraseña")) {
            verify = false;
        } else if (jTextField1.getText().equals("Usuario")) {
            verify = false;
        } else if (jPasswordField1.getText().equals("Contraseña")) {
            verify = false;
        } else {
            verify = true;
        }
        return verify;
    }

