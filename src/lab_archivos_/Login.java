    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            // TODO add your handling code here:
            if (User.checkUser(this.getUsername(), this.getPassword()) == true && dataCheck() == true) {
                JOptionPane.showMessageDialog(null, "Logged in!");
                switch (User.getUserType(this.getUsername())) {
                    case 0:
                        ClientDashboard dashboard = new ClientDashboard(this.getUsername());
                        dashboard.setVisible(true);
                        this.setVisible(false);
                        break;
                    case 1:
                        VetDashboard dashboard2 = new VetDashboard(this.getUsername());
                        dashboard2.setVisible(true);
                        this.setVisible(false);
                        break;
                    case 2:
                        AdminDashboard dashboard3 = new AdminDashboard(this.getUsername());
                        dashboard3.setVisible(true);
                        this.setVisible(false);
                        break;
                }

            } else {
                JOptionPane.showMessageDialog(null, "Datos incorrectos");
            }
            //System.out.println(this.getPassword());
        } catch (IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (java.lang.ArrayIndexOutOfBoundsException excp) {
            JOptionPane.showMessageDialog(null, "Usuario Inexistente");
        }
    }//GEN-LAST:event_jButton1MouseClicked

